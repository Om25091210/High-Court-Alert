package in.aryomtech.cgalert.Fragments.model;

public class stationData {
    String name;
    String num;

    public stationData(String name, String num) {
        this.name = name;
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public String getNum() {
        return num;
    }
}
